<p>Atsu-Taro &eacute; a dist&acirc;ncia entre este e o sistema de apoio, devido &agrave;s  perdas de calor na tubagem de interliga&ccedil;&atilde;o. Por  esta raz&atilde;o o Atsu-Taro deve ser colocado o mais perto poss&iacute;vel do sistema de apoio e a  tubagem de interliga&ccedil;&atilde;o ser  isolada termicamente.<br>
  Os valores apresentados para a frac&ccedil;&atilde;o  solar no gr&aacute;fico abaixo foram calculados atrav&eacute;s de um software de simula&ccedil;&atilde;o do desempenho t&eacute;rmico do  sistema Atsu-Taro tomando  como base os seguintes valores:</p>
<ul>
  <li>Consumo di&aacute;rio de &aacute;gua quente de 200 litros; </li>
  <li>Temperatura de consumo 45 &deg;C; </li>
  <li>Colectores inclinados de 40&deg; em rela&ccedil;&atilde;o &agrave; horizontal  e orientados a Sul; </li>
</ul>
