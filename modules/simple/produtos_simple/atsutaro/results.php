<p><strong>RESULTADOS ENERG&Eacute;TICOS</strong></p>
<p>A  energia solar m&eacute;dia  anual fornecida pelo Atsu-Taro para uma  fam&iacute;lia t&iacute;pica portuguesa (4  pessoas)  &eacute; de:</p>
<ul>
  <li>Porto - 7040,9  MJ/ano; </li>
  <li>Lisboa-7132,1  MJ/ano; </li>
  <li>Faro - 7562,8  MJ/ano. </li>
</ul>
<p>O Atsu-Taro garante em m&eacute;dia entre 70  a 90% das  necessidades energ&eacute;ticas para qualquer lugar do territ&oacute;rio nacional. Entre Abril e Setembro, o Atsu-Taro &eacute; praticamente aut&oacute;nomo  e, por isso,n&atilde;o necessita de qualquer outra fonte de energia auxiliar para o aquecimento da &aacute;gua.<br>
  Outro factor que deve ser levado em conta no desempenho do sistema solar t&eacute;rmico</p>
